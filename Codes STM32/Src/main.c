/**
 * @brief UART receive complete callback.
 * @param huart UART handle
 * @retval None
 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (huart->Instance == USART6) {
		// Place received data into circular buffer
		rxBuffer[rxBufferHead] = rxBuffer[0];
		rxBufferHead = (rxBufferHead + 1) % RX_BUFFER_SIZE;

		// Start receiving data on USART6 again
		HAL_UART_Receive_IT(&huart6, rxBuffer, 1);
	}
}

void lever_servomoteur( angle) {
	int cpt;
	for (cpt = 0; cpt < angle; cpt++) {
		TIM1->CCR1 += 10;
		HAL_Delay(10);
	}
}

void baisser_servomoteur( angle) {
	int cpt;
	for (cpt = 0; cpt < angle; cpt++) {
		TIM1->CCR1 -= 10;
		HAL_Delay(10);
	}
}


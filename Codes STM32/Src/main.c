/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {
	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_DMA_Init();
	MX_USART1_UART_Init();
	MX_USART6_UART_Init();
	MX_TIM3_Init();
	MX_ADC1_Init();
	MX_TIM1_Init();
	/* USER CODE BEGIN 2 */

	/* reception USART6 connecté vers la PI */
	HAL_UART_Receive_IT(&huart6, rxBuffer, 1);

	/* initialisation TIM1 en PWM */
	HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
	TIM1->CCR1 = 600; 			//initialisation du servomoteur à 0°

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {

		if (rxBufferHead != rxBufferTail) {
			// Copy data from circular buffer to linear buffer
			uint8_t size =
					(rxBufferHead >= rxBufferTail) ?
							(rxBufferHead - rxBufferTail) :
							(RX_BUFFER_SIZE - rxBufferTail);
			memcpy(rxCircularBuffer, &rxBuffer[rxBufferTail], size);
			rxBufferTail = (rxBufferTail + size) % RX_BUFFER_SIZE;

			// transmission avec l UART 1 vers la carte serializer
			HAL_UART_Transmit(&huart1, rxCircularBuffer, size, HAL_MAX_DELAY);
		}

		HAL_ADC_Start(&hadc1); // Démarre la conversion ADC

		if (HAL_ADC_PollForConversion(&hadc1, 100) == HAL_OK) // Attend que la conversion soit terminee

				{

			adcValue = HAL_ADC_GetValue(&hadc1); // Lit la valeur ADC convertie

		}

		tension = (float) adcValue * 3.3 / 4095; // Conversion adcValue en tension

		HAL_ADC_Stop(&hadc1); // Arrete l'ADC

		if (tension >= 1)		//si on detecte un obstacle a la distance voulu

				{
			HAL_UART_Transmit(&huart6, (uint8_t*) "stop\n", 6, HAL_MAX_DELAY); // on envoi un message au raspberry afin de mettre un message d'alerte sur l appli

			descente = 0;		// variable autorisant la descente remise à 0

			if (position == 0) {				// si la tête du robot est à 0°
				lever_servomoteur(90);		// alors on lêve la tete à 90°
				position = 90;// mise à jour de la variable position de la tête pour éviter de continuer a monter en boucle si il detecte encore un obstacle
			}

		}

		if (tension < 1) {					// si on ne detecte pas d'obstacle
			if (descente == 0) {				// si on autorise la descente
				baisser_servomoteur(90);		// alors la tete se baisse
				descente += 1;// on autorise plus la descente pour eviter que la tete descende constamment lorsque l'on detecte rien
				position = 0;// mise a jour de la position de la tete à 0° pour reautoriser la monter de tete si on detecte un obstacle
			}

		}
		HAL_Delay(10); // Délai pour la boucle, remets à jour toutes les 10ms

	}
	/* USER CODE END WHILE */

	/* USER CODE BEGIN 3 */

	/* USER CODE END 3 */
}

